    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        //check email field is empty
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email address!", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            //search where the email in the database user table
            String searchEmail = txtEmail.getText();
            String SQL = "SELECT * FROM User WHERE email = '" + searchEmail + "'";
            try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection connection = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/HotelManagementSystem", "root", "1234");
            java.sql.Statement stm = connection.createStatement();
                // Execute the query
                try (java.sql.ResultSet rs = stm.executeQuery(SQL)) {
                    if (rs.next()) {
                        System.out.println("Hello!");
                        txtQuestion.setText(rs.getString("question"));
                    } else {
                        JOptionPane.showMessageDialog(this, "Email not found.","ERROR",JOptionPane.ERROR_MESSAGE);
                        txtQuestion.setText("");
                    }
                }
                    //exception handling
                    } catch (ClassNotFoundException ex) {
                        System.out.println("Driver Not Found");
                    } catch (SQLException ex) {
                        System.out.println(ex.getMessage());
                    }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //check the fields are empty
        if(!txtEmail.getText().isEmpty() && !txtQuestion.getText().isEmpty() && !txtAnswer.getText().isEmpty() && !txtPassword.getText().isEmpty() && !txtTest.getText().isEmpty()){
        String password = txtPassword.getText();
        if (password.length() >= 8) {
        //update the password in the database user table 
        String SQL = "UPDATE User SET password = '" + password + "' WHERE email = '" + txtEmail.getText() + "'";
        // Execute the query
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection connection = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/HotelManagementSystem", "root", "1234");
            java.sql.Statement stm = connection.createStatement();
            
            int rowsUpdated = stm.executeUpdate(SQL);
            
            if (rowsUpdated > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Password updated successfully!");
                setVisible(false);
                new signIn().setVisible(true);
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Unsuccessfully...Customer Not Found!","Error",JOptionPane.ERROR_MESSAGE);
            }
        //exception handling   
        } catch (ClassNotFoundException ex) {
            System.out.println("Driver Not Found");
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        
        } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Password must be 8 characters long.", "Error", JOptionPane.ERROR_MESSAGE);
                txtPassword.setText("");
            }
        
        }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Save Failed !\n Please fill in all required fields.", "Error", JOptionPane.ERROR_MESSAGE);
            clearFields();
        }
    }//GEN-LAST:event_btnSaveActionPerformed


    private void btnSearchAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchAnswerActionPerformed
        //check the answer field is empty
        if (txtAnswer.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your answer for the required question!", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                //search the answer column related to the user input from database
                String searchAnswer = txtAnswer.getText();
                String SQL1 = "SELECT * FROM User WHERE answer = '" + searchAnswer + "'";
                try {
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    java.sql.Connection connection = java.sql.DriverManager.getConnection("jdbc:mysql://localhost/HotelManagementSystem", "root", "1234");
                    java.sql.Statement stmt = connection.createStatement();
                    try (java.sql.ResultSet rst = stmt.executeQuery(SQL1)) {
                        if (rst.next()) {
                            System.out.println("Correct Answer!");
                            txtTest.setText("Your answer is correct.");
                            } else {
                                javax.swing.JOptionPane.showMessageDialog(this, "Incorrect Answer!\n Please enter the correct answer you entered before.", "ERROR", JOptionPane.ERROR_MESSAGE);
                                txtEmail.setText("");
                                txtQuestion.setText("");
                                txtAnswer.setText("");
                            }
                    //exception handling
                    } catch (SQLException ex) {
                    Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
                }
            //exception handling                   
            } catch (ClassNotFoundException ex) { 
                Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
    }//GEN-LAST:event_btnSearchAnswerActionPerformed
